#Addition
def add(a ,b): #function name must start with letter or underscore.
	c = a + b
	print ("add is:",c)
	return c
add(20, 30)  #Function Call:- You can many times call function.
add (45, 45) #you can add many argumentsas you want.

#subtraction
def sub(a , b):
	c = a - b
	print ("sub is:",c)
sub(45, 25) #45, 25 this value store in 'a' and 'b', then execute operation "45 - 25 = 20".

#Division
def div(a , b):
	c = a / b
	print ("div is:",c)
div(100, 4)

#Multiplication
